package com.recruitos.candidate.dto;

import java.io.Serializable;

/**
 * DTO for querying candidates
 */
public class CandidateQueryDTO implements Serializable {

    private static final long serialVersionUID = 1L;

    /** Filter by name (fuzzy match) */
    private String name;

    /** Filter by phone */
    private String phone;

    /** Filter by current company (fuzzy match) */
    private String company;

    /** Filter by status */
    private String status;

    /** Filter by source */
    private String source;

    /** Filter by associated job ID */
    private Long jobId;

    /** Page number (default 1) */
    private Integer pageNum = 1;

    /** Page size (default 10) */
    private Integer pageSize = 10;

    // Getters and Setters

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public Long getJobId() {
        return jobId;
    }

    public void setJobId(Long jobId) {
        this.jobId = jobId;
    }

    public Integer getPageNum() {
        return pageNum;
    }

    public void setPageNum(Integer pageNum) {
        this.pageNum = pageNum;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }
}
